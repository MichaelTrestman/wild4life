<h3>How it works on the ground: Zimbabwe</h3>
<div class="header_divider"></div>
<img src="img/results/subphoto_4.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
<p>Wild4life partners with the Painted Dog Conservation project (PDC) in a remote corner of Matabeleland in western Zimbabwe. PDC employs about 50 local people in its conservation efforts and is a well established entity with local communities in the area. This semi-arid region has a highly dispersed and impoverished rural population with very high HIV prevalence and virtually no access to healthcare services, including services for HIV. For the most part, this is a practical issue. For example, the distance to the only local clinic, at Hwange, about 50 miles from the project. Transport is either unavailable or unaffordable for most people and few service providers ever visit the area.</p>
<div id="results4">
<p>With the medical staff of the clinic at Hwange, St Patricks, Wild4life has established a mobile clinic system, staffed by local doctors and counselors, that now routinely visit three previously unserved sites in the area. They offer VCT, HIV treatment and care, and treatment for other commonly associated diseases. In the first year of operation, as a direct result of the program, over 18,000 people have visited the mobile clinics, many seeing a doctor for the first time. 2500 people have tested for HIV at the sites. Of those, 712 proved to be infected with HIV and now are receiving treatment and routine follow-up as part of the on-going program.</p>

<p>PDC has implemented condom distribution, a peer-to-peer education initiative, and a male circumcision program in conjunction with Population Services International.</p>

<p>Currently, we're in discussion with the Elizabeth Glaser Pediatric AIDS Foundation in Zimbabwe to establish a PMTCT program at the PDC sites.</p>

<p>PDC has joined forces with Wilderness Safaris in the area, a regional safari operator, and the Zimbabwe National Parks service to increase the reach of the program.</p>

<p>Wild4life supports local medical staff with stipends to supplement meager state salaries, and has assisted with a range of issues that they face, including provision of basic medical materials and diagnostic equipment.</p>

<p>The cost in Zimbabwe has been around $11 per participant in the program, and this under some of the most extreme conditions that we've encountered anywhere in Africa. It's proving a cost-effective way of reaching large numbers of people in remote rural areas and of making efficient use of resources already in-country.</p>

