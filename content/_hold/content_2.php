<h3>Results at a glance</h3>
<div class="header_divider"></div>
<img src="img/results/subphoto_1.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
<p>Since Wild4life's inception in 2008, we have established programs at multiple sites in 12 countries in sub-Saharan Africa. Our model is scalable, easily replicable, and provides a cost-effective alternative for delivering health services to unserved rural areas in Africa. We work with state health services, Population Services International, and the Elizabeth Glaser Pediatric AIDS Foundation and other NGOs to deliver an array of services.</p>

<div id="results1">
<p>We have set up mobile clinics to reach unserved communities in areas where health services were previously unavailable, offering routine HIV testing and treatment, and treatment for other common diseases.</p>

<p>We have developed a method using group dynamics that results in very high rates of voluntary HIV testing amongst program participants &mdash; over 85%.</p>

<p>We have established regularized HIV community programs, including HIV education, recurring testing, prevention initiatives, ART, and male circumcision. PMTCT is in the works.</p>

<p>We have measured adherence to treatment in the longer-term as high as 88% due to an active case management program in communities that we work with.</p>

<p>The program has already made lasting change. Our cost-effective model gets people into the cycle of education and treatment. Once they're in, they become part of the solution. The best part is that it's cheap, at less than $10 / person.</p>

</div>

