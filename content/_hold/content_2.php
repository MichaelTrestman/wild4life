<h3>Changing social norms</h3>
<div class="header_divider"></div>
<img src="img/results/subphoto_3.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
<p>AIDS is a silent epidemic &mdash; in many communities it's not talked about freely and people affected by the disease have to deal with social isolation, stigma, and discrimination. Not talking about the disease often leaves people misinformed about the disease itself and how it can be treated. As a result they are reluctant to get tested and the cycle is perpetuated. Wild4life's intervention gives people practical information about HIV and, by mobilizing communities around testing, starts the discussion that is vital to changing the way that a community thinks about HIV and health in general.</p>
<div id="results3">
<p>We've been surprised by how fast attitudes towards people affected by the disease, and social norms subscribed to by communities, can change. It starts with getting people to do something concrete, like get tested for HIV, then using this to open a broader, on-going discussion that spreads through social networks in communities. It really can change the way a community thinks.</p>
</div>
