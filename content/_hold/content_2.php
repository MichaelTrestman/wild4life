<h3>Monitoring and evaluation</h3>
<div class="header_divider"></div>
<img src="img/results/subphoto_2.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
<p>Wild4life strongly believes in measuring our impact on the communities that we serve. Monitoring and evaluation isn't just for reporting, it's a tool to refine what we do. Also, we're committed to sharing what we learn, and the delivery model, with other organizations.</p>

<div id="results2">
<p>We collect "hard" data, for example, the number of people participating in programs, or the number of people testing positive for HIV and going on to treatment, but we're also interested in the softer aspects, such as how the programs are changing social behaviours that put people at risk, or reducing discrimination against people living with HIV. It's as important for us to understand the social effects of our programs on the communities that we work with, as it is to collect the "hard" data. Wild4life's monitoring and evaluation program is built around the program's components. We're interested in what's working and what's not (and why).</p>
