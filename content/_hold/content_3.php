<a href="#" rel="toggle[cameroon]" data-openimage="img/common/expander_button.gif" data-closedimage="img/common/expander_button.gif" onmouseover="mapimg.src='img/where/map/cameroon.gif';" onmouseout="mapimg.src='img/where/map/africa.gif';"><h4><div class="box" style="background: #a867ab;"></div>Cameroon <img src="img/common/expander_button.gif" alt="" title="" border="0" class="expander_right"></h4></a>
