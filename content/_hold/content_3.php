<div id="tanzania" class="sub_content">
<p>We partner with the Wildlife Conservation Society at four different sites in Tanzania: Mbeya in southwest Tanzania, Iringa in central Tanzania, Arusha in northern Tanzania, and Zanzibar, an island off the coast of the mainland. In the past, Tanzania's biodiversity was threatened due to habitat destruction, agricultural expansion, and energy demands. In recent years the Wildlife Conservation Society is helping to develop community-based initiatives through which local people will benefit from key habitats and have an interest in their long-term survival and integrity.</p>
</div>

