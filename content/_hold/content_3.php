<h4>Zimbabwe</h4>
<p>My name is Gertrude Moyo* and am 47 years. I have three children 26, 18 and 12 years old. I have been married and recently have been separated with my husband. I work for Painted Dog Conservation in the administration office and am a member of our HIV program.</p>

<div id="story2">
<p>I have been tested and am proud to know my status. Before I was tested, it worried me a lot, just thinking whether I was positive or not. Testing gave me a peace of mind and actually allowed me to sleep peacefully at night.</p>

<p>I did not have enough courage to go to the clinic and test but when Forgie, our HIV co-ordinator, started the Wild4life program with St Patricks Hospital, we thought she was crazy and wanted to get rid of us at work. However the support that the program gives us and assurance from our Management team helped us to understand that life does not end with HIV but can be a new beginning to a better life.</p>

<p>We women at PDC are all friends and we have learnt that we can support each other. We have formed our own family and encourage those who need treatment not to miss the visits from the St Patricks Hospital doctors.</p>

<p>My HIV positive friends are a great, strong pillar to me. Their drive and understanding for life is completely changed making them better people than before or than I am. Their attitude and drive towards life has turned around and they actually seem to live life at its fullest.</p>

<p>If anyone had to visit PDC, you would not be able to tell who is positive and who is negative. HIV has become an open topic to both men and women.</p>

<p>Testing should not be only done when you think you are ill but should be a requirement at anytime even if you are not ill. Testing and counseling will help you to understand HIV and help you accept other people that have it.
HIV is now a disease that is found in almost every family and is difficult to understand if you have not been counseled or tested yourself.</p>

<p>Gone are the days that HIV will make you ill or kill you. You can beat it and ARV's will allow you to have a normal life. The amazing thing about ARV's is that you can outlive some people that are negative if you are on ARV's.
I am always talking for my children about HIV and I would like to encourage other woman to know their status, teach our children and support our friends.</p>

<p>I want to thank Painted Dog Conservation and Wild4life for making this possible.</p>

