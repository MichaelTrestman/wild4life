<h4>Rwanda</h4>
<p>My name is Banda Kirezi*, I have been working with WCS since 1980s. I am living in the village called Banda which is neighboring Nyungwe National Park in Rwanda. I knew that I am HIV positive before Wild4life came to our organization and was difficult to access treatment as I was supposed to do many hours walking going either Kibogora hospital which is far away from my home or Cyato health center.</p>

<div id="story3">
<p>After knowing that I am HIV positive, I was scared that I was going to lose my job as it requires physical fitness and I was starting to feel very weak as my CD4 was reduced. I started taking treatment but it was difficult to access hospitals. It is after the Wild4life came to work closely with our organization that I got help from them to access treatment by providing transport but also Wild4life helped a lot in education of myself and my wife in different education session we had and that helped to understand well what measure to take and how to manage conflict within family due to my HIV status. I appreciate a lot these kind of education sessions with our family that are prepared with support from Wild4life as they give us more knowledge and skills about HIV/AIDS but also the way they help people to know their status by doing voluntary testing every year. Now I am doing well and fulfilling my tasks as normal.</p>

<p>Nyungwe National Park, Rwanda</p>

