</div>

<!-- Nigeria -->
<a href="#" rel="toggle[nigeria]" data-openimage="img/common/expander_button.gif" data-closedimage="img/common/expander_button.gif" onmouseover="mapimg.src='img/where/map/nigeria.gif';" onmouseout="mapimg.src='img/where/map/africa.gif';"><h4><div class="box" style="background: #981a6b;"></div>Nigeria <img src="img/common/expander_button.gif" alt="" title="" border="0" class="expander_right"></h4></a>
<div id="nigeria" class="sub_content">
