<h3>Personal Stories</h3>
<div class="header_divider"></div>


<img src="img/where/profile_1.jpg" alt="" title="" border="0" align="left" class="subphoto">
<h4>Zimbabwe</h4>
<p>I am Mrs Sophie Dube*. I am 39 years old, I have four children and my husband died of HIV last year. I had not known about my status until Painted Dog Conservation started the out reach program in Lupote.</p>
<div id="story1">
<p>It was not easy at first to go to the nearest clinic to my home, which is in Dete, so I went to Lupote Clinic, where none of the nurses knew me. At that time I was very ill and had come to ask for help for food from Painted Dog. My husband was not showing any signs of illness and I had TB.</p>

<p>My husband and I were both unemployed, my husband did odd jobs here and there while I sell tomatoes and fruits to passing buses on the highway and so we could not afford to travel the 100km to Hwange Town, to St Patrick's Hospital for testing or treatment. One thing I have learnt is that people don't want to know you when you are ill.
My CD4 was 13 when I started ARV's and I also got to a stage when I could not walk. In the meantime, my husband had no sings or symptoms at all. I have been married to only one man in my life. I was married to him when I was 16, which is normal in my world.</p>

<p>8 months down the line after taking my ARV's, I began to put on weight and finally when you look at me today, I am nice, pretty looking, round as what African women should be. Today as I speak to you my CD4 is 784.</p>

<p>When I spoke to my husband and asked him to get tested and get treatment, his answer was that he does not have too, as he was not ill, but that did not last long. Early last year he became so ill and by the time be was willing to test, it was too late and I buried him late last year.</p>

<p>I am sure that there are a lot of women out there with the same story, our cultural ways are very different and women have to abide by their husbands and sometimes that is not in our favor.</p>

<p>To date I have never missed treatment since I know my life and my children's lives depends on it. My attitude towards appreciating life has changed. I had given up hope and now its like I have been born again or given a new chance. I see life from a different angle.</p>

<p>HIV is a serous disease in our communities, which has touched each and every family and continues to affect us every day of our lives. Our rural communities are worst affected. Poverty, lack of education and cultures are difficult subjects to address and are difficult wars to win.</p>

<p><i>* Names have been changed to protect the person concerned from possible discrimination.</i></p>

