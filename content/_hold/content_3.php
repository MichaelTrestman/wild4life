<a href="#" rel="toggle[ethiopia]" data-openimage="img/common/expander_button.gif" data-closedimage="img/common/expander_button.gif" onmouseover="mapimg.src='img/where/map/ethiopia.gif';" onmouseout="mapimg.src='img/where/map/africa.gif';"><h4><div class="box" style="background: #5248a5;"></div>Ethiopia <img src="img/common/expander_button.gif" alt="" title="" border="0" class="expander_right"></h4></a>
<div id="ethiopia" class="sub_content">
<p>We partner with the Wildlife Conservation Network on the Ethiopia Wolf Conservation Program (EWCP) in northern Ethiopia. The EWCP alleviates threats to the critically endangered Ethiopian wolves by creating a comprehensive education program for school children and employing many local residents, which in turn raises the standard of living and fosters a strong conservation ethic among those who most closely coexist with this rare carnivore.</p>
</div>

<!-- Gabon -->
<a href="#" rel="toggle[gabon]" data-openimage="img/common/expander_button.gif" data-closedimage="img/common/expander_button.gif" onmouseover="mapimg.src='img/where/map/gabon.gif';" onmouseout="mapimg.src='img/where/map/africa.gif';"><h4><div class="box" style="background: #71be64;"></div>Gabon <img src="img/common/expander_button.gif" alt="" title="" border="0" class="expander_right"></h4></a>
<div id="gabon" class="sub_content">
<p>We partner with the Wildlife Conservation Society in Gabon. The Society supports conservation in seven out of the thirteen national parks by encouraging rural communities to protect their natural resources from exploitation.</p>
</div>

<!-- Kenya -->
<a href="#" rel="toggle[kenya]" data-openimage="img/common/expander_button.gif" data-closedimage="img/common/expander_button.gif" onmouseover="mapimg.src='img/where/map/kenya.gif';" onmouseout="mapimg.src='img/where/map/africa.gif';"><h4><div class="box" style="background: #b11e46;"></div>Kenya <img src="img/common/expander_button.gif" alt="" title="" border="0" class="expander_right"></h4></a>
<div id="kenya" class="sub_content">
<p>In Kenya, we partner with the One Acre Fund near Bungoma in western Kenya and the Lewa Wildlife Conservancy near Isiolo in eastern part of the country.</p>

<p>The One Acre Fund helps the poorest farmers to grow their way out of hunger by providing agricultural education and training, capital, access to markets, and crop insurance. One Acre currently serves 54,000 farmers in Kenya.</p>

<p>The Lewa Wildlife Conservancy works as a catalyst for the conservation of wildlife and its habitat. It does this through the protection and management of species, the initiation and support of community conservation and development programs, and the education of neighboring areas in the value of wildlife.</p>
</div>

<!-- Mozambique -->
<a href="#" rel="toggle[mozambique]" data-openimage="img/common/expander_button.gif" data-closedimage="img/common/expander_button.gif" onmouseover="mapimg.src='img/where/map/mozambique.gif';" onmouseout="mapimg.src='img/where/map/africa.gif';"><h4><div class="box" style="background: #0075b1;"></div>Mozambique <img src="img/common/expander_button.gif" alt="" title="" border="0" class="expander_right"></h4></a>
<div id="mozambique" class="sub_content">
