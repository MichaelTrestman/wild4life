<h3>What's in it for field partners and service providers?</h3>
<div class="header_divider"></div>
<img src="img/about/what/subphoto_3.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
We enable existing health service providers to increase their reach and impact by providing a ready-made delivery infrastructure in areas that they usually don't operate, connecting them with rural communities, and generating demand. We solve the dual problems of a delivery infrastructure in rural areas and demand creation in dispersed rural populations. Our field partners, e.g. conservation or agricultural NGOs, or safari companies, benefit through the improved health of their staff and gain a critical new service to provide to communities where they operate.
