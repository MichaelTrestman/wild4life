<h3>Mobilize communities and create demand</h3>
<div class="header_divider"></div>
<img src="img/about/what/subphoto_1.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
We've found that communities will mobilize around HIV, and in most of the areas that we work, HIV is the big health issue. It provides a focus to organize communities around and creates momentum by starting to get people to talk about health issues. We get people involved around HIV testing, this gets those that need it on to treatment and launches HIV prevention activities in the community. As the community begins to organize around HIV, we layer on a second wave of health programs aimed at diseases like TB and malaria. Our experience has been that it's hard for supply to keep up with demand as communities realize the benefits these programs can have.
