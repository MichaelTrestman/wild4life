<h3>Connect communities to providers</h3>
<div class="header_divider"></div>
<img src="img/about/what/subphoto_2.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
Health services don't just happen. In the rural areas, the dual problems of a delivery infrastructure and demand generation in dispersed rural populations need to be addressed first. A co-ordinated response works best for both communities and providers. For communities it means benefiting from an array of health services, and for providers, it means not having to incur the costs of developing their own infrastructure in rural areas, nor having to do their own demand creation, with the costs that involves. Wild4life provides both of these aspects and co-ordinates providers to deliver a range of health services in areas where none previously existed.
