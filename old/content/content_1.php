<h3>Our Story</h3>
<div class="header_divider"></div>
<img src="img/about/main/subphoto_3.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
Wild4life started as an HIV prevention and treatment program aimed at wildlife conservation workers in countries with high HIV prevalence in sub-Saharan Africa. We soon realized that the model we had developed and tested with conservation organizations could have much wider application &mdash; it wasn't just effective at addressing HIV. It could be used to deliver an array of health services. Wild4life has become a rural health program focusing on rural communities outside the reach of conventional health systems, connecting these unserved communities to much needed services.
