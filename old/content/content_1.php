<h3>Our Philosophy</h3>
<div class="header_divider"></div>
<img src="img/about/main/subphoto_1.jpg" alt="" title="" border="0" align="left" class="subphoto">
<div style="position: relative; float: left; width: 555px;">
<h4>Wild4life's model is based on two main ideas:</h4>
<p>That in Africa there is a mismatch between where health resources are and where they are most needed. Most people &mdash; 70% &mdash; live in the rural areas while most health services are in the urban centers. There's good reason for this: the rural population is dispersed and the distances vast. The logistical headache and enormous cost of developing a healthcare infrastructure to reach rural communities is prohibitive. But there is an alternative.</p>

<div id="about1">
<p>There is an infrastructure already established in many rural areas, organizations like conservation or agricultural NGOs, or safari companies, have developed a network of resources that can be leveraged to create a delivery platform for health services. These organizations also have trusting relationships with local communities that can be used to mobilize communities and drive demand.</p>
</div>

