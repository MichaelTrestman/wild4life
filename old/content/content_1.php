<div style="position: relative; float: left; width: 555px;">
<p>Wild4life's mission is to bring health services to rural African communities. We work with communities in rural areas in Africa, usually, beyond the reach of the traditional health services. We establish a delivery infrastructure in these unserved areas by leveraging what's already there, mobilizing rural communities to create demand, and connecting these communities to state and NGO providers &mdash; to deliver health services in areas where none previously existed.</p>
