<h3>How it Works</h3>
<div class="header_divider"></div>

<p>Wild4life is a rural health program in sub-Saharan Africa. Like most of the world, healthcare in Africa is organized around regional hospitals in the cities and a system of satellite clinics. But in Africa, most of the population &mdash; 70% &mdash; live in rural areas outside the reach of this system, often with little or no access to healthcare. These are the people we reach. </p>

<p>Wild4life connects health providers to these remote communities. Working with field partners already established in these areas, we leverage their resources to create a delivery platform, and then generate demand. 
In most of the areas that we work, HIV is the big issue. Also, we've found that communities will mobilize around HIV&mdash; which is why this is our initial focus.</p>

<p>We start by establishing a comprehensive HIV prevention and treatment program. This creates momentum and gets the community organized. We follow this with a second wave of programs, targeting other common maladies such as TB and malaria, maternal and child health, reproductive health, and other related activities like family planning.
The program is community driven, and makes good use of resources and an infrastructure that already exists in rural areas &mdash; to deliver health services. We bridge the gap between where resources are and where the need is. The best part is that it's cheap, at less than $10 / person. </p>

